// i18n de contenido (A2a): devuelve el banco con name/desc/tip traducidos
// cuando locale==='en' (overlay exercises.en.ts). steps/variants → A2b.
// IDs/muscleGroup/equipment/etc. nunca cambian.
export function getExercises(lang: AppLanguage): Exercise[] {
  if (lang !== 'en') return exercises;
  return exercises.map((ex) => {
    const o = exercisesEn[ex.id];
    // A2b: títulos de pasos + nombres de variantes (siempre, no dependen de `o`).
    const steps = ex.steps?.map((s) => {
      const title = stepTitleEn[s.title] ?? s.title;
      const desc = stepDescEn[s.desc] ?? s.desc;
      return title !== s.title || desc !== s.desc ? { ...s, title, desc } : s;
    });
    const variants = ex.variants?.map((v) => {
      const name = variantNameEn[v.name] ?? v.name;
      const notes = v.notes ? (variantNotesEn[v.notes] ?? v.notes) : v.notes;
      return name !== v.name || notes !== v.notes ? { ...v, name, notes } : v;
    });
    return {
      ...ex,
      ...(o?.name ? { name: o.name } : {}),
      ...(o?.desc ? { desc: o.desc } : {}),
      ...(o?.tip ? { tip: o.tip } : {}),
      ...(steps ? { steps } : {}),
      ...(variants ? { variants } : {}),
    };
  });
}
